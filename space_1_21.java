public class space_1_21 {
    public static void main(String[] args) throws Exception {
        for(int i=1;i<=5;i++)
        {
            for(int k=5;k>i;k--)
                System.err.print(" ");
            
            for(int j=1;j<=i;j++)
                System.err.print(j);
        
        System.out.println();
        
    }
}
}
