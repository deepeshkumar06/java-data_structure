public class space_5_45 {
    public static void main(String[] args) throws Exception {
        for(int i=5;i>=1;i--)
        {
            for(int k=i-1;k>=1;k--)
                System.err.print(" ");
            
            for(int j=i;j<=5;j++)
                System.err.print(j);
        
        System.out.println();
        
    }
}
}